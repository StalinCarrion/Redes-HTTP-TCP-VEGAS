    private void startbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startbtnActionPerformed
        int port = Integer.parseInt(portTXF.getText());
        Thread sockServer;
        sockServer = new Thread() {
        @Override
        public void run() {
            
                myServe.startServer(port);
            }
        };
        
        if (this.startbtn.getText().equalsIgnoreCase("Iniciar")) {         
            sockServer.start();
            this.statePanel.setBackground(Color.green);
            this.stateLBL.setText("Servido ejecutandose");
            this.startbtn.setText("DETENER");
            ManagerTXFS(false);
        }else{
            sockServer.stop();
            this.stateLBL.setText("Servido detenido");
            this.statePanel.setBackground(null);
            this.startbtn.setBackground(null);
            this.startbtn.setForeground(null);
            this.startbtn.setText("INICIAR");
            ManagerTXFS(true);
            
        }
    }//GEN-LAST:event_startbtnActionPerformed

    
    private void ManagerTXFS(boolean flag){
        this.hostTXF.setEnabled(flag);
        this.portTXF.setEnabled(flag);
    }
    
